#include "rtv.h"

_Bool	valid_hex(char *str)
{
	if (str && !(ft_strlen(str) % 2) && ft_strlen(str) <= 8)
	{
		while (*str)
		{
			if (!(*str >= '0' && *str <= '9') &&
				!(*str >= 'a' && *str <= 'f') &&
				!(*str >= 'A' && *str <= 'F'))
				return (0);
			str++;
		}
		return (!0);
	}
	return (0);
}
